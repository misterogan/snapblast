$(document).on('keyup', 'input', function (){
    $(this).next('small').addClass('hide');
})

$(document).on('mouseover', '.history-side', function (){
    $('body').addClass('overflow-hidden');
})

$(document).on('mouseleave', '.history-side', function (){
    $('body').removeClass('overflow-hidden');
})

const set_navigation = function (id){
    $("#"+id).addClass('router-link-active router-link-exact-active');
    return true;
};

const to_number = function(value){
    if(isNaN(value)){
        return '0';
    }
    let val = (value/1).toFixed(0).replace('.', ',')
    return val.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ".")
};

